CREATE TABLE IF NOT EXISTS users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS works (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS files (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    work_id INTEGER FOREIGN KEY REFERENCES works (id) ON DELETE CASCADE,
    student_id INTEGER FOREIGN KEY REFERENCES users(id) ON DELETE CASCADE,
    original_file_name VARCHAR (250) NOT NULL,
    s3_key VARCHAR (50) UNIQUE NOT NULL,
    mime_type VARCHAR(100),
    file_size BIGINT NOT NULL,
    uploaded_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP

    CONSTRAINT unique_student_work
        UNIQUE (student_id, work_id)
);